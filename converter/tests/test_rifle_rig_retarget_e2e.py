def _resolve_real_rig_fact(
    tmp_path: Path, *, field: str, child: str, class_name: str
) -> tuple[ChildRefScript, str]:
    """Drive the REAL producer chain (``build_child_ref_map`` ->
    ``_resolve_script`` -> ``_resolve_rig_facts``) over a real ``.cs`` file + a
    parsed hierarchy, returning the resolver-produced ``ChildRefScript`` (whose
    ``rig_facts`` was computed by the resolver, NOT hand-fabricated) and the
    canonical path key. Exercises the producer END for the producer->consumer
    ->verifier chain."""
    cs = (
        "using UnityEngine;\n"
        f"public class {class_name} : MonoBehaviour {{\n"
        f"  Transform cam; public Transform {field};\n"
        "  void Awake() {\n"
        "    cam = Camera.main.transform;\n"
        f"    {field} = cam.GetChild(0);\n"
        "  }\n"
        "}\n"
    )
    cs_path = tmp_path / f"{class_name}.cs"
    cs_path.write_text(cs, encoding="utf-8")
    info = ScriptInfo(
        path=cs_path, class_name=class_name, base_class="MonoBehaviour"
    )
    crm = build_child_ref_map(
        script_infos=[info],
        parsed_scenes=None,
        prefab_library=_fps_library(child=child, class_name=class_name),
        guid_index=_guid_index(cs_path),
    )
    key = str(cs_path.resolve())
    entry = crm.get(key)
    assert entry is not None, "the real producer must map the host script"
    return entry, key


class PipelineGate(TypedDict):
    """The REAL pipeline-gate outcome surface a contract breach produces."""

    errors: list[str]  # the contract-prefixed errors promoted onto ``ctx.errors``
    success: bool  # the real ``len(ctx.errors) == 0`` conversion-success flag
    rows: list[dict[str, object]]  # the recorded shadow-metric violation rows


def _run_pipeline_gate(scripts: list[RbxScript], tmp_path: Path) -> PipelineGate:
    """Drive the REAL ``Pipeline._run_contract_verifier`` hook (the same gate the
    corpus uses) and report the LOUD end-to-end failure surface read OFF THE REAL
    PIPELINE STATE — ``pipeline.ctx.errors`` (the promotion the hook performs) and
    the ``success`` flag (``len(ctx.errors) == 0``, pipeline.py:4340) — NOT a
    parallel re-computation. A hook that stopped promoting rows to ``ctx.errors``
    would flip ``success`` and DROP the contract errors, failing the boundary
    tests."""
    unity_project = tmp_path / "unity"
    (unity_project / "Assets").mkdir(parents=True)
    output = tmp_path / "out"
    output.mkdir()
    pipeline = Pipeline(str(unity_project), str(output))
    pipeline.state.rbx_place = RbxPlace()
    pipeline.state.rbx_place.scripts = scripts
    pipeline.ctx.scene_runtime = {}
    pipeline._run_contract_verifier({"topology": _TOPOLOGY})
    rows = pipeline.ctx.scene_runtime.get("contract_check_violations", [])
    # Read the REAL promotion off ``ctx.errors`` (what flips success), filtered to
    # this verifier's owned prefix; ``success`` is the pipeline's own predicate.
    contract_errs = [
        e for e in pipeline.ctx.errors if e.startswith(CONTRACT_ERROR_PREFIX)
    ]
    success = len(pipeline.ctx.errors) == 0
    return PipelineGate(errors=contract_errs, success=success, rows=rows)


