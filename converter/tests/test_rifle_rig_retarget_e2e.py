def _extract_method(
    luau_source: str, class_name: str, method: str, *, terminator: str
) -> str:
    """Extract the EXACT ``function <Class>:<method>() ... <terminator> ... end``
    block the REAL lowering emitted (not a re-typed copy), so the harness exercises
    shipped Luau. ``terminator`` is the literal final STATEMENT line of the method
    body (e.g. ``    return slot``); the method's closing ``end`` (the first ``end``
    at the start of a line AFTER the terminator) is included so the block is a
    complete, loadable function."""
    m = re.search(
        rf"function {re.escape(class_name)}:{re.escape(method)}\(\)"
        rf".*?\n{re.escape(terminator)}[^\n]*\nend",
        luau_source,
        re.S,
    )
    assert m is not None, f"could not extract {class_name}:{method} from the lowering"
    return m.group(0)


def _lowered_resolver_and_consumer(
    *, field: str = "weaponSlot", child: str = "WeaponSlot", class_name: str = "Player"
) -> tuple[str, str]:
    """Run the REAL lowering and extract BOTH the emitted resolver method AND the
    REWRITTEN consumer ``GetRifle`` (where ``self.<field>`` reads became
    ``self:_resolve<Child>()`` calls). Returns ``(resolver_method, consumer_method)``
    — both verbatim from the live lowering output."""
    lowered = _lower(
        _ai_output_shape(class_name=class_name, field=field),
        field=field,
        child=child,
    )
    resolver = _extract_method(
        lowered.luau_source, class_name, f"_resolve{child}", terminator="    return slot"
    )
    consumer = _extract_method(
        lowered.luau_source, class_name, "GetRifle", terminator="    return rifle"
    )
    # Sanity: the lowering MUST have rerouted the consumer's reads (the load-bearing
    # discharge). If a regression left a raw ``self.<field>`` read, the harness
    # below would resolve nil and FAIL — but assert it here too for a clear message.
    assert f"self:_resolve{child}()" in consumer, (
        "the lowered consumer must reroute its reads through the resolver"
    )
    assert f"self.{field}" not in consumer, (
        "no raw self.<field> read may survive in the rerouted consumer"
    )
    return resolver, consumer


# A deterministic Roblox-API stub: ``task.wait`` is the clock. The rig Model is
# inserted into ``workspace`` ON a chosen tick INSIDE the bounded retry, so the
# first ``findSlot()`` sweep MISSES (Awake-with-rig-absent) and the retry catches
# the late-streaming rig. The whole schedule is forced — no real time, no flake.
# The harness drives the REWRITTEN CONSUMER (GetRifle), so the rifle seats via the
# consumer's own ``self:_resolve<Child>()``-rerouted reads — the load-bearing path.
def _race_harness(
    resolver_method: str,
    consumer_method: str,
    *,
    rig_appears_on_tick: int,
    child: str = "WeaponSlot",
    class_name: str = "Player",
) -> str:
    return f"""
-- ===== Deterministic Roblox-API stub (task.wait is the clock) =====
local WORKSPACE_CHILDREN = {{}}
local TICK = 0
local RIG_APPEARS_ON_TICK = {rig_appears_on_tick}

local function makeInstance(className, attrs)
    local inst
    inst = {{
        _class = className,
        _attrs = attrs or {{}},
        _children = {{}},
        Parent = nil,
        IsA = function(self, c) return self._class == c end,
        GetAttribute = function(self, k) return self._attrs[k] end,
        GetChildren = function(self) return self._children end,
        GetPivot = function(self) return self end,
        PivotTo = function(self, _cf) end,
        FindFirstChild = function(self, name, _recursive)
            local function walk(node)
                for _, ch in node._children do
                    if ch._name == name then return ch end
                    local found = walk(ch)
                    if found then return found end
                end
                return nil
            end
            return walk(self)
        end,
    }}
    return inst
end

-- The rig Model + its slot child (the late-streaming target).
local weaponSlot = makeInstance("BasePart", {{}})
weaponSlot._name = "{child}"
local rig = makeInstance("Model", {{ _MainCameraRig = true }})
rig._name = "_MainCameraRig"
weaponSlot.Parent = rig
rig._children = {{ weaponSlot }}

-- workspace:GetDescendants() flattens whatever has streamed in so far.
local workspace = {{
    CurrentCamera = makeInstance("Camera", {{}}),
    GetDescendants = function(self)
        local out = {{}}
        local function collect(node)
            for _, ch in node._children do
                table.insert(out, ch)
                collect(ch)
            end
        end
        for _, top in WORKSPACE_CHILDREN do
            table.insert(out, top)
            collect(top)
        end
        return out
    end,
}}

-- task.wait is the deterministic clock: it advances TICK and, on the chosen
-- tick, STREAMS THE RIG IN (the late-arrival the bounded retry exists for).
local task = {{
    wait = function(_dt)
        TICK = TICK + 1
        if TICK == RIG_APPEARS_ON_TICK then
            table.insert(WORKSPACE_CHILDREN, rig)
        end
        return _dt
    end,
}}

-- ``pivotOf`` is nil-safe and expects a real Instance (the on-corpus :13 helper).
local function pivotOf(obj)
    if not obj then return "ORIGIN" end
    if obj.GetPivot then return obj:GetPivot() end
    return "ORIGIN"
end

-- ``self.host.instantiatePrefab`` seats the rifle under the resolved slot (the
-- runtime host: ``clone.Parent = parent or workspace``, scene_runtime.luau:2025).
local function makeHost()
    return {{
        instantiatePrefab = function(_prefab, parent, _pivot)
            local clone = makeInstance("Model", {{}})
            clone.Parent = parent  -- a real slot Instance, or nil -> workspace
            return clone
        end,
    }}
end

-- ===== The REAL emitted resolver + REWRITTEN consumer, verbatim =====
local {class_name} = {{}}
{class_name}.__index = {class_name}
{resolver_method}
{consumer_method}

-- ===== Drive the failing schedule THROUGH the consumer =====
local self = setmetatable({{}}, {class_name})
self.host = makeHost()
self.riflePrefab = makeInstance("Model", {{}})

-- (1) Awake runs with the rig ABSENT (no _MainCameraRig in workspace yet).
assert(#workspace:GetDescendants() == 0, "rig must be absent at Awake")

-- (2) equip fires: the REWRITTEN GetRifle calls self:_resolve{child}(); the first
--     sweep MISSES, the bounded retry runs, the rig streams in on the chosen tick,
--     and the consumer parents the rifle under the resolved slot.
local rifle = self:GetRifle()

-- (3) assert the rifle ends up parented under the rig's slot (NOT nil, NOT
--     workspace-origin) — the read reroute seated the binding under the race.
assert(rifle ~= nil, "FAIL: GetRifle returned nil")
assert(rifle.Parent ~= nil, "FAIL: rifle did not seat under any slot (read not rerouted?)")
assert(rifle.Parent._name == "{child}", "FAIL: rifle seated under the wrong child")
assert(rifle.Parent.Parent == rig, "FAIL: the slot is not under the _MainCameraRig")
assert(rifle.Parent.Parent:GetAttribute("_MainCameraRig") == true,
    "FAIL: parent is not the rig")

-- (4) memoization: a SECOND GetRifle on the SAME instance reuses the cached slot,
--     and a FRESH instance resolves its OWN slot (per-instance, no shared state).
local rifle2 = self:GetRifle()
assert(rifle2.Parent == rifle.Parent, "FAIL: resolver is not memoized per-instance")
local other = setmetatable({{}}, {class_name})
other.host = makeHost()
other.riflePrefab = makeInstance("Model", {{}})
local other_rifle = other:GetRifle()
assert(other_rifle.Parent == weaponSlot, "FAIL: second instance did not resolve the slot")

print("RACE_OK")
"""


def _run_luau(source: str, timeout: float = 20.0) -> "subprocess.CompletedProcess[str]":
    f = tempfile.NamedTemporaryFile(
        mode="w", suffix=".luau", delete=False, encoding="utf-8"
    )
    path = f.name
    try:
        f.write(source)
        f.close()
        return subprocess.run(
            ["luau", path], capture_output=True, text=True, timeout=timeout
        )
    finally:
        Path(path).unlink(missing_ok=True)


def _luau_available() -> bool:
    from shutil import which

    return which("luau") is not None


