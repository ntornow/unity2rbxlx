class TestStickyOptInPersistsThroughResume:
    """Codex R1-P1.A: ``--allow-nonplayable-output`` must survive a
    resume rebuild even when the operator doesn't re-pass the flag.
    The front-door commands implement this as "sticky opt-in":
    explicit True overrides persisted; False (Click default) leaves
    the persisted value alone.

    These tests cover the ctx-level invariant; the front-door command
    behavior is validated by the integration tests in this file's
    plumbing class above.
    """

    def test_persisted_true_survives_unset_cli_flag(self):
        # Simulate the resume flow: ctx loaded from disk carries the
        # opt-in; CLI flag default is False; sticky logic must NOT
        # overwrite the persisted value.
        ctx = ConversionContext()
        ctx.allow_nonplayable_output = True

        # Sticky-opt-in mirror of the front-door code: only assign
        # when the CLI explicitly opted in.
        cli_flag = False  # operator didn't re-pass --allow-nonplayable-output
        if cli_flag:
            ctx.allow_nonplayable_output = True

        assert ctx.allow_nonplayable_output is True, (
            "persisted opt-in must survive a resume without the flag"
        )

    def test_explicit_true_overrides_persisted_false(self):
        ctx = ConversionContext()
        ctx.allow_nonplayable_output = False
        cli_flag = True
        if cli_flag:
            ctx.allow_nonplayable_output = True
        assert ctx.allow_nonplayable_output is True


