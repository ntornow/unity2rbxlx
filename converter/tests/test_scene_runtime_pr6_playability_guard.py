class TestAllowNonplayableEscape:

    def test_escape_downgrades_invalid_module(self, tmp_path):
        # Same plan as ``test_invalid_domain_fails`` but with the
        # escape set -- the guard logs + writes UNCONVERTED.md, no
        # raise.
        plan = {
            "modules": {
                "guid-bad": {
                    "stem": "Bad",
                    "runtime_bearing": True,
                    "domain": "shared",
                    "module_path": "ReplicatedStorage.Bad",
                },
            },
            "scenes": {}, "prefabs": {}, "domain_overrides": {},
        }
        p = _make_pipeline_with_ctx(
            tmp_path, "generic", plan, allow_nonplayable=True,
        )
        for s in _host_scripts():
            p.state.rbx_place.scripts.append(s)
        # Must not raise.
        p._check_runtime_playability_guard()
        # Warning staged for downstream UNCONVERTED.md write.
        warnings = p.ctx.scene_runtime.get("nonplayable_warnings")
        assert isinstance(warnings, list) and len(warnings) == 1
        w = warnings[0]
        assert w["runtime_bearing_count"] == 1
        assert len(w["invalid_modules"]) == 1
        assert w["invalid_modules"][0]["script_id"] == "guid-bad"
        assert w["missing_host_scripts"] == []
        assert w["scene_runtime_mode"] == "generic"

    def test_escape_downgrades_missing_host(self, tmp_path):
        p = _make_pipeline_with_ctx(
            tmp_path, "generic", _valid_runtime_plan(),
            allow_nonplayable=True,
        )
        # No host scripts appended.
        p._check_runtime_playability_guard()
        warnings = p.ctx.scene_runtime["nonplayable_warnings"]
        assert warnings[0]["missing_host_scripts"] == [
            "SceneRuntime",
            "SceneRuntimePlan",
            "SceneRuntimeClient",
            "SceneRuntimeServer",
        ]

    def test_escape_renders_in_unconverted_md(self, tmp_path):
        # End-to-end: escape staged warning is rendered by the
        # downstream _write_unconverted_md.
        p = _make_pipeline_with_ctx(
            tmp_path, "generic", _valid_runtime_plan(),
            allow_nonplayable=True,
        )
        p._check_runtime_playability_guard()
        p._write_unconverted_md()
        unconverted = tmp_path / "UNCONVERTED.md"
        assert unconverted.exists(), (
            "guard downgrade must surface the warning into UNCONVERTED.md"
        )
        body = unconverted.read_text(encoding="utf-8")
        assert "Non-playable converted place" in body
        assert "--allow-nonplayable-output" in body
        assert "SceneRuntime" in body  # at least one missing host script listed

    def test_escape_without_guard_fire_does_not_write_warning(self, tmp_path):
        # Operator passed the flag but the run was actually playable
        # -- no warning should be staged.
        #
        # Codex R1-P2: the guard resets ``nonplayable_warnings`` to
        # an empty list at entry (so stale warnings from prior runs
        # never re-render). A clean run therefore leaves the list
        # PRESENT but empty -- semantically "the most recent run
        # asserted no playability issues". _write_unconverted_md
        # treats an empty list as no content (no section emitted).
        p = _make_pipeline_with_ctx(
            tmp_path, "generic", _valid_runtime_plan(),
            allow_nonplayable=True,
        )
        for s in _host_scripts():
            p.state.rbx_place.scripts.append(s)
        p._check_runtime_playability_guard()
        assert p.ctx.scene_runtime.get("nonplayable_warnings") == []
        # No UNCONVERTED.md write either -- the empty list must not
        # produce a stale "Non-playable" section.
        p._write_unconverted_md()
        unconverted = tmp_path / "UNCONVERTED.md"
        assert not unconverted.exists() or (
            "Non-playable" not in unconverted.read_text(encoding="utf-8")
        )


