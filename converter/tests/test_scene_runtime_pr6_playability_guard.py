class TestHardFailures:

    def test_no_module_plan_at_all_fails(self, tmp_path):
        # Runtime-bearing module declared with no domain / no path.
        plan = {
            "modules": {
                "guid-foo": {
                    "stem": "Foo",
                    "runtime_bearing": True,
                    # no domain, no module_path -- PR3b classifier
                    # never ran (e.g. exception in _classify_storage)
                },
            },
            "scenes": {}, "prefabs": {}, "domain_overrides": {},
        }
        p = _make_pipeline_with_ctx(tmp_path, "generic", plan)
        # Host emit also missing (the subphase's own early-out would
        # have caught a planless run; we test the combined surface).
        with pytest.raises(RuntimeError) as exc_info:
            p._check_runtime_playability_guard()
        msg = str(exc_info.value)
        assert "PR6 playability guard" in msg
        assert "Foo" in msg or "guid-foo" in msg
        assert "--allow-nonplayable-output" in msg
        assert "--scene-runtime=legacy" in msg

    def test_invalid_domain_fails(self, tmp_path):
        plan = {
            "modules": {
                "guid-bad-domain": {
                    "stem": "BadDomain",
                    "runtime_bearing": True,
                    "domain": "shared",  # invalid; valid set is {client, server}
                    "module_path": "ReplicatedStorage.BadDomain",
                },
            },
            "scenes": {}, "prefabs": {}, "domain_overrides": {},
        }
        p = _make_pipeline_with_ctx(tmp_path, "generic", plan)
        for s in _host_scripts():
            p.state.rbx_place.scripts.append(s)
        with pytest.raises(RuntimeError) as exc_info:
            p._check_runtime_playability_guard()
        assert "domain=" in str(exc_info.value)
        assert "'shared'" in str(exc_info.value)

    def test_missing_module_path_fails(self, tmp_path):
        plan = {
            "modules": {
                "guid-no-path": {
                    "stem": "NoPath",
                    "runtime_bearing": True,
                    "domain": "client",
                    # module_path missing
                },
            },
            "scenes": {}, "prefabs": {}, "domain_overrides": {},
        }
        p = _make_pipeline_with_ctx(tmp_path, "generic", plan)
        for s in _host_scripts():
            p.state.rbx_place.scripts.append(s)
        with pytest.raises(RuntimeError) as exc_info:
            p._check_runtime_playability_guard()
        assert "module_path" in str(exc_info.value)

    def test_missing_host_scripts_fails(self, tmp_path):
        # Plan is valid; the host emit never happened (e.g. host
        # runtime file deleted from disk). The guard catches the
        # missing engine even when the plan claims everything is fine.
        p = _make_pipeline_with_ctx(
            tmp_path, "generic", _valid_runtime_plan(),
        )
        # Intentionally do NOT append host scripts.
        with pytest.raises(RuntimeError) as exc_info:
            p._check_runtime_playability_guard()
        msg = str(exc_info.value)
        assert "SceneRuntime" in msg
        assert "Missing SceneRuntime host scripts" in msg

    def test_partial_host_emit_fails(self, tmp_path):
        # Only SceneRuntime + SceneRuntimePlan present; both
        # entrypoints missing (e.g. autogen generator crashed
        # mid-emit). The guard treats partial as incomplete.
        p = _make_pipeline_with_ctx(
            tmp_path, "generic", _valid_runtime_plan(),
        )
        for s in _host_scripts()[:2]:  # first two only
            p.state.rbx_place.scripts.append(s)
        with pytest.raises(RuntimeError) as exc_info:
            p._check_runtime_playability_guard()
        msg = str(exc_info.value)
        assert "SceneRuntimeClient" in msg
        assert "SceneRuntimeServer" in msg


