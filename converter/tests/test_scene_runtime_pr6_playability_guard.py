class TestSubphaseOrder:
    """The order of write_output's subphases is load-bearing. Document
    and assert that the guard sits between ``_subphase_inject_scene_runtime``
    and the artifact emit subphases that follow it."""

    def test_subphase_order_places_guard_after_host_emit(self):
        order = Pipeline.SUBPHASE_ORDER
        assert "_subphase_inject_scene_runtime" in order
        assert "_check_runtime_playability_guard" in order
        host_idx = order.index("_subphase_inject_scene_runtime")
        guard_idx = order.index("_check_runtime_playability_guard")
        assert guard_idx == host_idx + 1, (
            "guard must run immediately after the host emit subphase so "
            "a clean generic run has its host scripts staged before the "
            "predicate fires"
        )

    def test_subphase_order_places_guard_before_finalize(self):
        order = Pipeline.SUBPHASE_ORDER
        guard_idx = order.index("_check_runtime_playability_guard")
        # Any subphase that writes the artifact (encode_terrain,
        # finalize_scripts_to_disk) must follow the guard.
        finalize_idx = order.index("_subphase_finalize_scripts_to_disk")
        assert guard_idx < finalize_idx


