"""PR6: write_output hard playability guard rail tests.

Covers ``Pipeline._check_runtime_playability_guard`` -- the safety net
that lands before PR7's default ``--scene-runtime=auto`` flip. The guard
ensures that under ``generic`` / ``auto`` modes the converter cannot
silently emit a non-playable place (runtime-bearing MonoBehaviours with
no valid plan + SceneRuntime host emit).

Test matrix from the design doc (PR6 row + line ~732):
  * Guard fires only under ``generic``/``auto``; legacy +
    resume/preserved-script unaffected.
  * ``--allow-nonplayable-output`` passes through with a warning.
  * Synthetic conversion with runtime-bearing MB + valid plan + valid
    host -> write_output succeeds under generic.
  * Synthetic conversion with runtime-bearing MB but NO plan ->
    write_output fails under generic; passes under
    ``--allow-nonplayable-output`` (with warning logged).
  * Same setup under legacy -> write_output succeeds.
  * Resume path with a complete cache -> guard doesn't re-fire.

PR7 cross-reference: PR7 flips the default mode to ``auto`` and relies
on this guard to backstop projects that lack a complete contract plan.
Its e2e test (no flag passed) MUST fail pre-PR6; once PR6 lands, the
fail surface migrates to this guard's RuntimeError. PR7 should land
strictly after PR6 (the design doc's merge order says exactly that;
this guard exists to make the flip safe).
"""

