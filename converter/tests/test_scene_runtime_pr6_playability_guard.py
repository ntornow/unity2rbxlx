class TestModeGating:
    """The guard MUST NOT fire under modes that never emit the host
    runtime, regardless of plan completeness. Pre-PR6 legacy / resume
    paths are byte-stable; firing here would regress that invariant."""

    def test_legacy_mode_is_noop_even_with_runtime_bearing(self, tmp_path):
        # Worst-case legacy: a runtime-bearing module in the plan but
        # no host emit. Legacy never honors the plan; guard short-
        # circuits before it could fail this run.
        p = _make_pipeline_with_ctx(
            tmp_path, "legacy", _valid_runtime_plan(),
        )
        # No host scripts in rbx_place -- mirrors a legacy assemble.
        p._check_runtime_playability_guard()  # must not raise

    def test_auto_mode_resolves_to_legacy_before_guard(self, tmp_path):
        # _check_auto_fail_closed rewrites scene_runtime_mode to
        # "legacy" on any fail-closed signal BEFORE the guard runs.
        # Simulate the post-fallback state: mode is "legacy" by the
        # time the guard sees ctx, so the guard must not fire even
        # if the plan still carries runtime-bearing rows.
        p = _make_pipeline_with_ctx(
            tmp_path, "legacy", _valid_runtime_plan(),
        )
        p._check_runtime_playability_guard()

    def test_empty_scene_runtime_under_legacy_is_noop(self, tmp_path):
        # Legacy + no plan at all (the default pre-PR1 shape). No
        # runtime-bearing modules to worry about.
        p = _make_pipeline_with_ctx(tmp_path, "legacy", {})
        p._check_runtime_playability_guard()

    def test_generic_no_runtime_bearing_modules_is_noop(self, tmp_path):
        # Generic mode but the plan carries only non-runtime-bearing
        # rows. The host emit subphase already short-circuited; the
        # guard must follow suit.
        plan = {
            "modules": {
                "guid-helper": {
                    "stem": "Helper",
                    "runtime_bearing": False,
                },
            },
            "scenes": {}, "prefabs": {}, "domain_overrides": {},
        }
        p = _make_pipeline_with_ctx(tmp_path, "generic", plan)
        p._check_runtime_playability_guard()


