def _make_pipeline_with_ctx(
    tmp_path: Path,
    scene_runtime_mode: str,
    scene_runtime: dict,
    allow_nonplayable: bool = False,
) -> Pipeline:
    """Minimal Pipeline for guard unit tests (mirrors the helper in
    ``test_scene_runtime_pipeline_emit.py``)."""
    p = Pipeline.__new__(Pipeline)
    p.ctx = ConversionContext(unity_project_path=str(tmp_path / "project"))
    p.ctx.scene_runtime_mode = scene_runtime_mode
    p.ctx.scene_runtime = scene_runtime
    p.ctx.allow_nonplayable_output = allow_nonplayable
    p.output_dir = tmp_path
    p.output_dir.mkdir(parents=True, exist_ok=True)

    state = MagicMock()
    state.rbx_place = RbxPlace()
    state.rbx_place.scripts = []
    state.animation_result = None
    state.transpilation_result = None
    state.material_mappings = {}
    state.rbx_place.unconverted_components = []
    p.state = state
    return p


def _valid_runtime_plan() -> dict:
    """A planner artefact carrying one runtime-bearing module with
    every PR3b field set -- the shape ``_subphase_inject_scene_runtime``
    consumes successfully under generic."""
    return {
        "modules": {
            "guid-foo": {
                "stem": "Foo",
                "runtime_bearing": True,
                "domain": "client",
                "module_path": "ReplicatedStorage.Foo",
            },
        },
        "scenes": {},
        "prefabs": {},
        "domain_overrides": {},
    }


def _host_scripts() -> list[RbxScript]:
    """The four scripts ``_subphase_inject_scene_runtime`` emits under
    generic with a runtime-bearing plan. Tests use these to simulate a
    clean post-host-emit state without having to invoke the subphase.

    Each script carries the autogen marker the real subphase stamps so
    the guard's marker-based "is this a real autogen emit?" check
    accepts them (codex R1-P1.B fix).
    """
    return [
        RbxScript(
            name="SceneRuntime",
            source="-- scene_runtime: PR4 generic host runtime\n",
            script_type="ModuleScript",
            parent_path="ReplicatedStorage",
        ),
        RbxScript(
            name="SceneRuntimePlan",
            source=(
                "-- SceneRuntimePlan (auto-generated by Unity converter; PR4)\n"
            ),
            script_type="ModuleScript",
            parent_path="ReplicatedStorage",
        ),
        RbxScript(
            name="SceneRuntimeClient",
            source=(
                "-- SceneRuntimeClient (auto-generated; "
                "PR4 scene-runtime host entrypoint)\n"
            ),
            script_type="LocalScript",
            parent_path="StarterPlayer.StarterPlayerScripts",
        ),
        RbxScript(
            name="SceneRuntimeServer",
            source=(
                "-- SceneRuntimeServer (auto-generated; "
                "PR4 scene-runtime host entrypoint)\n"
            ),
            script_type="Script",
            parent_path="ServerScriptService",
        ),
    ]


