class TestResumePath:
    """Rehydrate paths (``u2r publish``, ``convert_interactive upload``)
    reload ctx from disk and re-run write_output. A complete cache from
    a prior clean ``generic`` conversion must NOT trip the guard on
    rerun -- otherwise repeated upload calls would degrade into spurious
    failures."""

    def test_resume_with_complete_cache_passes(self, tmp_path):
        # Round-trip ctx through ConversionContext.save/load to model
        # what the resume path actually does.
        ctx_path = tmp_path / "conversion_context.json"
        ctx = ConversionContext(
            unity_project_path=str(tmp_path / "project"),
        )
        ctx.scene_runtime_mode = "generic"
        ctx.scene_runtime = _valid_runtime_plan()
        ctx.save(ctx_path)

        # Reload (matches u2r.publish / convert_interactive.upload).
        reloaded = ConversionContext.load(ctx_path)
        assert reloaded.scene_runtime_mode == "generic"
        assert reloaded.scene_runtime["modules"]["guid-foo"]["domain"] == "client"

        # Build a pipeline with the reloaded ctx + a fresh host emit
        # (which the rebuild's _subphase_inject_scene_runtime would
        # produce). Guard must accept this state.
        p = Pipeline.__new__(Pipeline)
        p.ctx = reloaded
        p.output_dir = tmp_path
        state = MagicMock()
        state.rbx_place = RbxPlace()
        state.rbx_place.scripts = list(_host_scripts())
        state.rbx_place.unconverted_components = []
        state.animation_result = None
        state.transpilation_result = None
        state.material_mappings = {}
        p.state = state
        # Default allow_nonplayable_output is False; cache is complete;
        # guard passes.
        p._check_runtime_playability_guard()

    def test_resume_persists_allow_flag(self, tmp_path):
        # The escape flag persists through ctx save/load so a subsequent
        # ``assemble`` or ``upload`` rerun without re-passing
        # --allow-nonplayable-output still honors the original opt-in.
        # This matches how scene_runtime_mode threads through resumes.
        ctx_path = tmp_path / "conversion_context.json"
        ctx = ConversionContext(
            unity_project_path=str(tmp_path / "project"),
        )
        ctx.scene_runtime_mode = "generic"
        ctx.allow_nonplayable_output = True
        ctx.scene_runtime = _valid_runtime_plan()
        ctx.save(ctx_path)
        reloaded = ConversionContext.load(ctx_path)
        assert reloaded.allow_nonplayable_output is True


